# Helper function to trigger ETL
def run_background_etl():
    logger.info("Running background database refresh...")
    pipeline = ETLPipeline()
    try:
        pipeline.run(force_download=False)
        logger.info("Background database refresh completed.")
    except Exception as e:
        logger.error(f"Error in background database refresh: {e}")
    finally:
        pipeline.close()

# Endpoints
@app.get("/stocks", response_model=List[StockResponse], tags=["Stocks"])
def get_stocks(db: Session = Depends(get_db)):
    """Retrieves all tracked stock symbols and details."""
    try:
        return db.query(Stock).all()
    except Exception as e:
        raise HTTPException(
            status_code=status.HTTP_500_INTERNAL_SERVER_ERROR,
            detail=f"Database error: {str(e)}"
        )

@app.get("/stock/{symbol}", response_model=List[MarketDataResponse], tags=["Stocks"])
def get_stock_data(symbol: str, limit: int = 100, db: Session = Depends(get_db)):
    """Retrieves historical market data for a given stock symbol."""
    stock = db.query(Stock).filter(Stock.ticker == symbol.upper()).first()
    if not stock:
        raise HTTPException(
            status_code=status.HTTP_404_NOT_FOUND,
            detail=f"Stock with ticker '{symbol}' not found."
        )
    try:
        return (
            db.query(MarketData)
            .filter(MarketData.stock_id == stock.id)
            .order_by(MarketData.timestamp.desc())
            .limit(limit)
            .all()
        )
    except Exception as e:
        raise HTTPException(
            status_code=status.HTTP_500_INTERNAL_SERVER_ERROR,
            detail=f"Database error: {str(e)}"
        )

