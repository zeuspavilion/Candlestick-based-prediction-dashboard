@app.post("/refresh", response_model=RefreshResponse, tags=["ETL"])
def trigger_refresh(background_tasks: BackgroundTasks):
    """Triggers the ETL pipeline asynchronously to pull new data."""
    try:
        background_tasks.add_task(run_background_etl)
        return {
            "status": "PROCESSING",
            "message": "ETL ingestion and indicator refresh started in background.",
            "timestamp": datetime.datetime.now()
        }
    except Exception as e:
        raise HTTPException(
            status_code=status.HTTP_500_INTERNAL_SERVER_ERROR,
            detail=f"Failed to start ETL pipeline: {str(e)}"
        )

