@app.get("/predictions", response_model=List[PredictionResponse], tags=["Predictions"])
def get_predictions(limit: int = 100, db: Session = Depends(get_db)):
    """Retrieves list of predictions generated by the deep learning pipeline."""
    try:
        results = (
            db.query(
                Prediction.id,
                Stock.ticker,
                Prediction.stock_id,
                Prediction.prediction_timestamp,
                Prediction.label_date,
                Prediction.current_close,
                Prediction.next_close,
                Prediction.next_day_return,
                Prediction.predicted_label,
                Prediction.true_label,
                Prediction.confidence,
                Prediction.correct
            )
            .join(Stock, Stock.id == Prediction.stock_id)
            .order_by(Prediction.prediction_timestamp.desc())
            .limit(limit)
            .all()
        )
        # Parse tuple results to dictionary response list
        predictions_list = []
        for r in results:
            predictions_list.append({
                "id": r.id,
                "ticker": r.ticker,
                "stock_id": r.stock_id,
                "prediction_timestamp": r.prediction_timestamp,
                "label_date": r.label_date,
                "current_close": r.current_close,
                "next_close": r.next_close,
                "next_day_return": r.next_day_return,
                "predicted_label": r.predicted_label,
                "true_label": r.true_label,
                "confidence": r.confidence,
                "correct": r.correct,
            })
        return predictions_list
    except Exception as e:
        raise HTTPException(
            status_code=status.HTTP_500_INTERNAL_SERVER_ERROR,
            detail=f"Database error: {str(e)}"
        )

@app.get("/analytics", response_model=AnalyticsSummaryResponse, tags=["Analytics"])
def get_market_analytics(db: Session = Depends(get_db)):
    """Executes Module 2 SQL queries to compile general market dashboard statistics."""
    try:
        top_gainers = execute_analytical_query(db, "top_gainers", {"limit": 5})
        top_losers = execute_analytical_query(db, "top_losers", {"limit": 5})
        highest_volume = execute_analytical_query(db, "highest_volume", {"limit": 5})
        market_breadth = execute_analytical_query(db, "market_breadth")
        avg_confidence = execute_analytical_query(db, "average_confidence")
        
        return {
            "top_gainers": top_gainers,
            "top_losers": top_losers,
            "highest_volume": highest_volume,
            "market_breadth": market_breadth,
            "average_confidence": avg_confidence,
        }
    except Exception as e:
        raise HTTPException(
            status_code=status.HTTP_500_INTERNAL_SERVER_ERROR,
            detail=f"Database error running SQL Analytics: {str(e)}"
        )

@app.get("/performance", response_model=PerformanceSummaryResponse, tags=["Performance"])
def get_model_performance(db: Session = Depends(get_db)):
    """Compiles predictions accuracy and lists model training runs history."""
    try:
        accuracy_data = execute_analytical_query(db, "prediction_accuracy")
        model_runs = db.query(ModelRun).order_by(ModelRun.run_timestamp.desc()).all()
        
        metrics = {}
        if accuracy_data:
            metrics = accuracy_data[0]
            
        return {
            "metrics": metrics,
            "model_runs": model_runs
        }
    except Exception as e:
        raise HTTPException(
            status_code=status.HTTP_500_INTERNAL_SERVER_ERROR,
            detail=f"Database error loading metrics: {str(e)}"
        )

